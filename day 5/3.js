function applycolor(p1){
    let ptag = document.querySelector("p");
    ptag.style.color=p1;
}

function applyfont(p1){
    let ptag = document.querySelector("p");
    ptag.style.fontSize=p1;
}


function applybackground(p1){
    let ptag = document.querySelector("p");
    ptag.style.background=p1;
}